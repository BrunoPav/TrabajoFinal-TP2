class Evento {
  constructor(nombre, lugar, fecha, horario, descripcion, modalidad, precio) {
    this.nombre = nombre;
    this.lugar = lugar;
    this.fecha = fecha;
    this.horario = horario;
    this.descripcion = descripcion;
    this.modalidad = modalidad;
    this.precio = precio;
  }

}

export default Evento;

